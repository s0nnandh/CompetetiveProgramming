ll mod = 1e9 + 7;
ll lcm(ll a,ll b){
    return (a * b) / gcd(a,b);
}
void solve(){
    ll n;
    cin >> n;
    ll p = 1,ptr = 2;
    ll ans = (2 * n) % mod;
    while(1){
        p = lcm(p,ptr);
        if(p > n)break;
        ans = (ans + (n / p)) % mod;        
        ++ptr;
    } 
    // ans = (ans + (n / ptr)) % mod;
    cout << ans;ln;
}

