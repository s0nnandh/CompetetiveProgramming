void solve(){
    int n;
    cin >> n;
    vector<ll> h(n);
    for(auto &i : h)cin >> i;
    vector<ll> give(n + 3);
    function<bool(int)> ok = [&](int x){
        give.assign(n + 3,0);
        for(int i = n - 1;i >= 2;--i){
            ll y = h[i] - x + give[i + 1] + (2 * give[i + 2]);
            if(y < 0)return false;
            give[i] = min(h[i] / 3,y / 3);
        }
        
        bool b = (h[0] + (2 * give[2])) >= x && ((h[1] + give[2] + 2 * give[3]) >= x); 
        return b;
    };
    int l = 1,r = 1e9 + 2,ans = 1;
    while(l <= r){
        int mid = (l + r) / 2;
        if(ok(mid)){
            ans = mid;
            l = mid + 1;
        }
        else r = mid - 1;
    }
    cout << ans << '\n';
}
 
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);    
    int test=1;
    cin>>test;   
    while(test--)
        solve();
    return 0;
}