template <typename T> 
T pow(T a, long long b) {
	assert(b >= 0);
	T r = 1; while (b) { if (b & 1) r *= a; b >>= 1; a *= a; } return r;
}

void solve(){
	int n,d;
	std :: cin >> n >> d;
	ModInt ans = 0,me = 1;
	for(int i = 1;i <= n;++i){
		if(2 * (n - i) < d)break;
		if(i + d <= n)ans += me * pow(ModInt(2),d);
		if(d >= 2)ans += me * (std :: min(2 * (n - i),2 * (d - 1)) - d + 1) * pow(ModInt(2),d - 2);
		me *= 2;
	}
	std :: cout << ans * 2 << '\n';
}

