	ll mul(ll a, ll b) {
	  return ((a % mod) * (b % mod)) % mod;
	}
	ll pow(ll base, ll exp) {
	  ll res = 1;
	  while (exp) {
		if (exp % 2 == 1){
			res = (res * base) % mod;
		}
		exp >>= 1;
		base = (base * base) % mod;
	  }
	  return res;
	}
	ll inv(ll base) {
	  return pow(base, mod - 2);
	}
	ll div(ll a, ll b) {
	  return mul(a, inv(b));
	}
	
